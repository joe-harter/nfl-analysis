def map_players_from_desc(players: set):
    """
    Assists a human user in identifying the appropriate player id given
    the player string from a play's description

    players: set - A string for a player like (##-F.Last)
    returns: dict - Returns a map of player string to nfl player id (gsis_id)
    """
    return_dict = {}
    nfl_player_df = get_players_info()
    for player in players:
        last_name = player.split(".")[1]
        first_initial = player.split(".")[0].split("-")[1]
        jersey_number = int(player.split("-")[0])
        possible_players = nfl_player_df[
            (nfl_player_df["last_name"] == last_name)
            & (
                (nfl_player_df["first_name"].str.startswith(first_initial))
                | (nfl_player_df["display_name"].str.startswith(first_initial))
            )
            & (nfl_player_df["entry_year"].notna())
        ]
        if possible_players.shape[0] == 1:
            return_dict[player] = possible_players.iloc[0]["gsis_id"]
        elif possible_players.shape[0] > 1:
            player_selected = False
            print(f"Multiple players found for {player}:")
            for index, possible_player in possible_players.iterrows():
                possible_name = possible_player["display_name"]
                answer = input(f"Is the player {possible_name}? (Y/N)")
                if answer == "Y":
                    player_selected = True
                    return_dict[player] = possible_player["gsis_id"]
                    break
            if not player_selected:
                print(f"WARNING: Player {player} not found.")
    return return_dict


